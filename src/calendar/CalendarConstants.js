export const sortAPI = APIres => {
  let sortedAPI = {};
  CALENDAR_TYPES.map(type => {
    if (APIres.data[type]) {
      let data = APIres.data[type].sort((x, y) => {
        return (
          moment(x.month_start, US_DATE_FORMAT) -
          moment(y.month_start, US_DATE_FORMAT)
        );
      });
      sortedAPI[type] = data;
    }
  });
  return sortedAPI;
};

